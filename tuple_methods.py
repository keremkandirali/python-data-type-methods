my_tuple = ('Apple', 'Orange', 'Banana')


my_tuple.count('Apple')  # my_tuple.count(x*)
# Counts an item in the tuple.
# 1


my_tuple.index('Banana')  # my_tuple.index(x*)
# Finds the index of the item in the tuple.
# Raises ValueError if not found.
# 2


# (*) required
